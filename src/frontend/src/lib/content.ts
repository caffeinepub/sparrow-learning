export type ExerciseType = "vocab" | "mcq" | "fill" | "match" | "translate";

export interface VocabExercise {
  type: "vocab";
  word: string;
  translation: string;
  pronunciation: string;
}
export interface McqExercise {
  type: "mcq";
  question: string;
  options: string[];
  answer: string;
}
export interface FillExercise {
  type: "fill";
  sentence: string;
  answer: string;
  hint: string;
}
export interface MatchExercise {
  type: "match";
  pairs: { word: string; translation: string }[];
}
export interface TranslateExercise {
  type: "translate";
  source: string;
  answer: string;
  hint: string;
}

export type Exercise =
  | VocabExercise
  | McqExercise
  | FillExercise
  | MatchExercise
  | TranslateExercise;

export interface Level {
  id: number;
  unit: number;
  unitTitle: string;
  levelTitle: string;
  type: ExerciseType;
  xpReward: number;
  exercises: Exercise[];
}

