function makeMcq(
  word: string,
  meaning: string,
  allVocab: [string, string, string][],
): McqExercise {
  const wrong = allVocab
    .filter((v) => v[0] !== word)
    .sort(() => Math.random() - 0.5)
    .slice(0, 3)
    .map((v) => v[1]);
  const options = [meaning, ...wrong].sort(() => Math.random() - 0.5);
  return {
    type: "mcq",
    question: `What does "${word}" mean?`,
    options,
    answer: meaning,
  };
}

function makeFill(
  word: string,
  meaning: string,
  pronunciation: string,
): FillExercise {
  return {
    type: "fill",
    sentence: `The word for "${meaning}" is ___`,
    answer: word,
    hint: `Pronunciation: ${pronunciation}`,
  };
}

function makeTranslate(word: string, meaning: string): TranslateExercise {
  return {
    type: "translate",
    source: meaning,
    answer: word,
    hint: "Think about how this word sounds",
  };
}

const levelCache = new Map<string, Level[]>();

export function getLevels(language: Language): Level[] {
  if (levelCache.has(language)) return levelCache.get(language)!;

  const units =
    language === "japanese"
      ? japaneseUnits
      : language === "mandarin"
        ? mandarinUnits
        : kannadaUnits;
  const levels: Level[] = [];
  let levelId = 1;

  // 50 units × 10 levels = 500 levels
  for (let unitIdx = 0; unitIdx < 50; unitIdx++) {
    const unit = units[unitIdx % units.length];
    const unitNumber = unitIdx + 1;
    const unitTitle =
      unitIdx < units.length
        ? unit.title
        : `${unit.title} — Advanced ${Math.floor(unitIdx / units.length) + 1}`;
    const allVocabForUnit = unit.vocab;

    for (let levelInUnit = 0; levelInUnit < 10; levelInUnit++) {
      const vocabIndex = levelInUnit % allVocabForUnit.length;
      const vocabSlice = allVocabForUnit.slice(vocabIndex, vocabIndex + 4);
      if (vocabSlice.length < 4)
        vocabSlice.push(...allVocabForUnit.slice(0, 4 - vocabSlice.length));

      const exercises: Exercise[] = [];
      const levelTypes: ExerciseType[] = [
        "vocab",
        "mcq",
        "fill",
        "match",
        "translate",
      ];
      const levelType = levelTypes[levelInUnit % levelTypes.length];

      // Always start with a vocab intro
      exercises.push({
        type: "vocab",
        word: vocabSlice[0][0],
        translation: vocabSlice[0][1],
        pronunciation: vocabSlice[0][2],
      });

      // MCQ
      exercises.push(
        makeMcq(vocabSlice[1][0], vocabSlice[1][1], allVocabForUnit),
      );

      // Fill in the blank
      exercises.push(
        makeFill(vocabSlice[2][0], vocabSlice[2][1], vocabSlice[2][2]),
      );

      // Match pairs
      exercises.push({
        type: "match",
        pairs: vocabSlice.map((v) => ({ word: v[0], translation: v[1] })),
      });

      // Translate
      exercises.push(makeTranslate(vocabSlice[3][0], vocabSlice[3][1]));

      const levelTitles = [
        "Introduction",
        "Practice",
        "Review",
        "Challenge",
        "Mastery",
        "Speed Round",
        "Deep Dive",
        "Quiz",
        "Fluency",
        "Expert",
      ];

      levels.push({
        id: levelId++,
        unit: unitNumber,
        unitTitle,
        levelTitle: levelTitles[levelInUnit],
        type: levelType,
        xpReward: Math.round(10 + levelId * 0.1),
        exercises,
      });
    }
  }

  levelCache.set(language, levels);
  return levels;
}

export function getLevel(
  language: Language,
  levelId: number,
): Level | undefined {
  return getLevels(language).find((l) => l.id === levelId);
}

